#!/usr/bin/env node
// Sync OpenCode session data (db + storage + tool-output) to/from GitHub.
// Cross-platform (macOS / Windows / Linux).
//
// MUST run OUTSIDE of OpenCode — this script kills OpenCode processes
// to safely copy the SQLite database.
//
// Usage:
//   node sync-sessions.js push [--force]
//   node sync-sessions.js pull [--force]
//   node sync-sessions.js status
//
// push: checkpoint + backup db, stage into git repo, rebase + push
// pull: fetch + reset, restore db + storage + tool-output
//
// --force on push: force push on rebase conflict
// --force on pull: discard local changes and overwrite

const fs = require("node:fs");
const path = require("node:path");
const os = require("node:os");
const { execSync } = require("node:child_process");
const { c, success, error, warn, info, step, note, formatStats, formatDiffStats, completionBanner, upToDateBanner } = require("./ui");

// ── Constants ────────────────────────────────────────────────────────

const BRANCH = "main";
const MAX_KILL_RETRIES = 3;
const KILL_WAIT_MS = 2000;

const SESSION_DATA_FILES = ["opencode.db"];
const SESSION_DATA_DIRS = ["storage", "tool-output"];

const DEFAULT_GITIGNORE = `# Runtime / generated
node_modules/
/package.json
/package-lock.json
bun.lock

# OS files
.DS_Store
Thumbs.db
desktop.ini

# Empty dirs (runtime-generated by plugins)
plugin/

# Data dir: skip binaries and logs (large, machine-specific)
_data/bin/
_data/log/
# SQLite WAL/SHM — we checkpoint before push, only track the main db
_data/opencode.db-shm
_data/opencode.db-wal
.gitignore

# Machine-specific identity (not synced)
.device-identity.json
`;

const DEFAULT_GITATTRIBUTES = `# Normalize line endings across platforms
* text=auto

# Git LFS — large binary files
_data/opencode.db filter=lfs diff=lfs merge=lfs -text
`;

// ── Remote URL ───────────────────────────────────────────────────────

function getRemoteUrl() {
  const url = process.env.SYNC_REMOTE_URL;
  if (!url) {
    error("SYNC_REMOTE_URL environment variable is not set.");
    console.error("   Set it to your GitHub repo URL, e.g.:");
    console.error("   export SYNC_REMOTE_URL=\"https://github.com/user/repo.git\"");
    process.exit(1);
  }
  return url;
}

// ── Path helpers ─────────────────────────────────────────────────────

function getConfigRoot() {
  if (process.env.SYNC_CONFIG_ROOT) return process.env.SYNC_CONFIG_ROOT;
  const xdg = process.env.XDG_CONFIG_HOME || path.join(os.homedir(), ".config");
  return path.join(xdg, "opencode");
}

function getDataRoot() {
  if (process.env.SYNC_DATA_ROOT) return process.env.SYNC_DATA_ROOT;
  const xdgData =
    process.env.XDG_DATA_HOME || path.join(os.homedir(), ".local", "share");
  return path.join(xdgData, "opencode");
}

// ── Helpers ──────────────────────────────────────────────────────────

function ensureDir(dir) {
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
}

function ensureFile(filePath, content) {
  if (!fs.existsSync(filePath)) {
    fs.writeFileSync(filePath, content);
    return true;
  }
  return false;
}

function rmFileIfExists(filePath) {
  if (fs.existsSync(filePath)) {
    fs.unlinkSync(filePath);
  }
}

function rmDirRecursive(dir) {
  if (fs.existsSync(dir)) {
    fs.rmSync(dir, { recursive: true, force: true });
  }
}

function copyFileIfExists(src, dst) {
  if (fs.existsSync(src)) {
    ensureDir(path.dirname(dst));
    fs.copyFileSync(src, dst);
    return true;
  }
  return false;
}

function copyDirRecursive(src, dst) {
  if (!fs.existsSync(src)) return 0;
  ensureDir(dst);
  let count = 0;
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const srcPath = path.join(src, entry.name);
    const dstPath = path.join(dst, entry.name);
    if (entry.isDirectory()) {
      count += copyDirRecursive(srcPath, dstPath);
    } else {
      fs.copyFileSync(srcPath, dstPath);
      count++;
    }
  }
  return count;
}

function timestamp() {
  return new Date().toISOString().replace(/T/, " ").replace(/\..+/, "");
}

function getPlatformName() {
  return process.platform === "win32"
    ? "windows"
    : process.platform === "darwin"
      ? "mac"
      : process.platform;
}

function commitMessage(prefix = "sessions") {
  const host = os.hostname();
  const plat = getPlatformName();
  return `${prefix}: ${timestamp()} from ${host} (${plat})`;
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

// ── Git helpers ──────────────────────────────────────────────────────

function git(args, opts = {}) {
  const cwd = opts.cwd || getConfigRoot();
  try {
    return {
      ok: true,
      out: execSync(`git ${args}`, {
        cwd,
        encoding: "utf8",
        stdio: "pipe",
        env: { ...process.env, GIT_TERMINAL_PROMPT: "0" },
      }).trim(),
    };
  } catch (e) {
    return {
      ok: false,
      out: (e.stdout || "").trim(),
      err: (e.stderr || "").trim(),
      code: e.status,
    };
  }
}

function isGitRepo(dir) {
  return fs.existsSync(path.join(dir, ".git"));
}

function ensureGitConfig(root) {
  const nameResult = git("config user.name", { cwd: root });
  if (!nameResult.ok || !nameResult.out) {
    git('config user.name "OpenCode Sync"', { cwd: root });
  }
  const emailResult = git("config user.email", { cwd: root });
  if (!emailResult.ok || !emailResult.out) {
    git('config user.email "sync@opencode.local"', { cwd: root });
  }
}

function skipLfs() {
  return process.env.SYNC_SKIP_LFS === "1";
}

function ensureLfs(root) {
  if (skipLfs()) return;
  const result = git("lfs install --local", { cwd: root });
  // silently ignore if git-lfs not available
}

function initRepo(root) {
  const remote = getRemoteUrl();
  step(`Initializing git repo: ${root}`);
  step(`Remote: ${remote}`);

  ensureFile(path.join(root, ".gitignore"), DEFAULT_GITIGNORE);
  ensureFile(path.join(root, ".gitattributes"), DEFAULT_GITATTRIBUTES);

  git(`init -b ${BRANCH}`, { cwd: root });
  git(`remote add origin "${remote}"`, { cwd: root });
  ensureGitConfig(root);
  ensureLfs(root);

  const fetchResult = git(`fetch origin ${BRANCH}`, { cwd: root });
  if (fetchResult.ok) {
    const revResult = git(`rev-parse origin/${BRANCH}`, { cwd: root });
    if (revResult.ok) {
      git(`reset --hard origin/${BRANCH}`, { cwd: root });
      git(`branch --set-upstream-to=origin/${BRANCH} ${BRANCH}`, { cwd: root });
      return "fetched";
    }
  }

  if (!fetchResult.ok) {
    warn(`Fetch failed (${fetchResult.err || "unknown error"}); starting from an empty repo`);
  }
  return "fresh";
}

function ensureRepo(root) {
  if (!isGitRepo(root)) {
    const mode = initRepo(root);
    return mode;
  }
  ensureGitConfig(root);
  ensureLfs(root);
  return "ready";
}

// ── Process detection & killing ──────────────────────────────────────

function findOpenCodePids() {
  const isWin = process.platform === "win32";
  try {
    if (isWin) {
      // On Windows, look for opencode.exe or node.exe running opencode
      const out = execSync(
        "powershell -Command \"Get-Process | Where-Object { $_.ProcessName -eq 'opencode' -or ($_.ProcessName -eq 'node' -and $_.MainModule.FileName -like '*opencode*') } | Select-Object -ExpandProperty Id\"",
        { encoding: "utf8", stdio: "pipe" },
      ).trim();
      if (!out) return [];
      return out
        .split(/\r?\n/)
        .map((s) => parseInt(s.trim(), 10))
        .filter(Boolean);
    } else {
      // On macOS/Linux, use pgrep
      const out = execSync('pgrep -f "opencode"', {
        encoding: "utf8",
        stdio: "pipe",
      }).trim();
      if (!out) return [];
      // Filter out our own process
      const myPid = process.pid;
      const parentPid = process.ppid;
      return out
        .split(/\r?\n/)
        .map((s) => parseInt(s.trim(), 10))
        .filter((pid) => pid && pid !== myPid && pid !== parentPid);
    }
  } catch {
    // pgrep/powershell returns non-zero when no match
    return [];
  }
}

function killPids(pids) {
  const isWin = process.platform === "win32";
  for (const pid of pids) {
    try {
      if (isWin) {
        execSync(`taskkill /F /PID ${pid}`, { stdio: "pipe" });
      } else {
        execSync(`kill -9 ${pid}`, { stdio: "pipe" });
      }
      console.log(`${c.yellow}  Killed PID ${pid}${c.reset}`);
    } catch {
      // Process may have already exited
    }
  }
}

async function ensureNoOpenCode() {
  // Skip process check in test environments
  if (process.env.SYNC_SKIP_PROCESS_CHECK === "1") return true;

  for (let attempt = 1; attempt <= MAX_KILL_RETRIES; attempt++) {
    const pids = findOpenCodePids();
    if (pids.length === 0) {
      if (attempt > 1) note("All background processes closed (processes terminated)");
      return true;
    }

    warn(`Found ${pids.length} running OpenCode processes, attempting to close... (${attempt}/${MAX_KILL_RETRIES})`);
    killPids(pids);

    // Wait for processes to die
    await sleep(KILL_WAIT_MS);
  }

  // Final check
  const remaining = findOpenCodePids();
  if (remaining.length > 0) {
    error(`Failed to terminate OpenCode after ${MAX_KILL_RETRIES} attempts.`);
    console.error(`   Remaining PIDs: ${remaining.join(", ")}`);
    console.error("   Please close OpenCode manually and retry.");
    return false;
  }

  console.log(`${c.green}  ✅ All OpenCode processes terminated.${c.reset}`);
  return true;
}

// ── SQLite safe copy (using .backup) ─────────────────────────────────

function backupSqlite(srcDbPath, dstDbPath) {
  // Use sqlite3 .backup for a consistent snapshot, even if db was recently held open.
  // .backup acquires a shared lock and copies page-by-page — safe against WAL state.
  try {
    execSync(`sqlite3 "${srcDbPath}" ".backup '${dstDbPath}'"`, {
      encoding: "utf8",
      stdio: "pipe",
    });
    return true;
  } catch (e) {
    error(`SQLite backup failed: ${e.message}`);
    return false;
  }
}

function verifySqlite(dbPath) {
  try {
    const result = execSync(`sqlite3 "${dbPath}" "PRAGMA integrity_check;"`, {
      encoding: "utf8",
      stdio: "pipe",
    }).trim();
    if (result === "ok") {
      return true;
    }
    warn(`Integrity check result: ${result}`);
    return false;
  } catch (e) {
    // Can't verify but don't block — sqlite3 CLI may not be installed
    return true;
  }
}

// ── Migration auto-repair ────────────────────────────────────────────
// When pulling an older db to a machine running newer OpenCode, the
// newer OpenCode will try to re-run migrations that the older db
// doesn't have records for — causing CREATE TABLE crashes on tables
// that already exist with different schemas.
//
// Fix: on push, export the full __drizzle_migrations table to
// db-meta.json. On pull, snapshot local migrations before overwriting
// the db, then INSERT any missing rows back. OpenCode sees all
// migrations as "already applied" and skips them.

const DB_META_FILE = "db-meta.json";
const SESSION_ORIGIN_FILE = "session-origin.json";

/**
 * Read all rows from __drizzle_migrations as an array of objects.
 * Returns [] if sqlite3 is unavailable or the table doesn't exist.
 */
function getMigrationRows(dbPath) {
  if (!fs.existsSync(dbPath)) return [];
  try {
    const raw = execSync(
      `sqlite3 -json "${dbPath}" "SELECT hash, created_at, name, applied_at FROM __drizzle_migrations;"`,
      { encoding: "utf8", stdio: "pipe" },
    ).trim();
    return raw ? JSON.parse(raw) : [];
  } catch {
    return [];
  }
}

/**
 * Read session rows for origin mapping.
 */
function getSessionRows(dbPath) {
  if (!fs.existsSync(dbPath)) return [];
  try {
    const raw = execSync(
      `sqlite3 -json "${dbPath}" "SELECT id, time_created, time_updated FROM session;"`,
      { encoding: "utf8", stdio: "pipe" },
    ).trim();
    return raw ? JSON.parse(raw) : [];
  } catch {
    return [];
  }
}

/**
 * Write db-meta.json with the full migration table for pull-side repair.
 */
function writeMigrationMeta(repoData, srcDbPath) {
  const migrations = getMigrationRows(srcDbPath);
  const meta = {
    migrations,
    hostname: os.hostname(),
    pushedAt: new Date().toISOString(),
  };
  fs.writeFileSync(
    path.join(repoData, DB_META_FILE),
    JSON.stringify(meta, null, 2) + "\n",
  );
  return meta;
}

/**
 * Write per-session origin metadata for cross-machine attribution.
 */
function writeSessionOriginMeta(repoData, srcDbPath) {
  const sessions = getSessionRows(srcDbPath);
  const generatedAt = new Date().toISOString();
  const host = os.hostname();
  const platform = getPlatformName();
  const map = {};

  for (const row of sessions) {
    if (!row || !row.id) continue;
    const updatedAtMs =
      row.time_updated !== null && row.time_updated !== undefined
        ? row.time_updated
        : row.time_created !== null && row.time_created !== undefined
          ? row.time_created
          : null;
    map[row.id] = {
      host,
      platform,
      updatedAtMs,
      pushedAt: generatedAt,
    };
  }

  const payload = {
    schemaVersion: 1,
    generatedAt,
    host,
    platform,
    sessions: map,
  };

  fs.writeFileSync(
    path.join(repoData, SESSION_ORIGIN_FILE),
    JSON.stringify(payload, null, 2) + "\n",
  );
  return payload;
}

/**
 * After pulling a remote db, repair missing migration records.
 * @param {string} dbPath - Path to the local (just-overwritten) db
 * @param {object[]} localRowsBefore - Migration rows from BEFORE overwrite
 */
function repairMigrations(dbPath, localRowsBefore) {
  if (!localRowsBefore.length) return 0;
  if (!fs.existsSync(dbPath)) return 0;

  // Read what the new (pulled) db has
  const newRows = getMigrationRows(dbPath);
  // Use `name` as the unique key (hash may be empty in some Drizzle versions)
  const newNames = new Set(newRows.map((r) => r.name).filter(Boolean));

  // Find rows that existed locally but are missing in the pulled db
  const missing = localRowsBefore.filter(
    (r) => r.name && !newNames.has(r.name),
  );
  if (!missing.length) return 0;

  // INSERT missing rows back
  try {
    for (const row of missing) {
      const hash = (row.hash || "").replace(/'/g, "''");
      const createdAt = row.created_at ?? "NULL";
      const name = `'${row.name.replace(/'/g, "''")}'`;
      const appliedAt = row.applied_at
        ? `'${row.applied_at.replace(/'/g, "''")}'`
        : `'${new Date().toISOString()}'`;
      execSync(
        `sqlite3 "${dbPath}" "INSERT INTO __drizzle_migrations (hash, created_at, name, applied_at) VALUES ('${hash}', ${createdAt}, ${name}, ${appliedAt});"`,
        { stdio: "pipe" },
      );
    }
    console.log(
      `${c.cyan}🔧 Migration auto-repair: restored ${missing.length} local records (remote db is older)${c.reset}`,
    );
    return missing.length;
  } catch (e) {
    warn(`Migration repair failed: ${e.message}`);
    console.error("   OpenCode may fail to start. If issues occur, push then pull again.");
    return 0;
  }
}

// ── Stage session data ───────────────────────────────────────────────

function stageSessionDataIn(root) {
  const dataRoot = getDataRoot();
  const repoData = path.join(root, "_data");
  let fileCount = 0;

  ensureDir(repoData);

  // Use sqlite3 .backup for db files instead of raw copy
  for (const file of SESSION_DATA_FILES) {
    const srcPath = path.join(dataRoot, file);
    const dstPath = path.join(repoData, file);
    if (!fs.existsSync(srcPath)) continue;

    if (file.endsWith(".db")) {
      // Safe backup
      if (!backupSqlite(srcPath, dstPath)) {
        error(`Backup of ${file} failed, aborting.`);
        process.exit(1);
      }
      // Verify the copy
      if (!verifySqlite(dstPath)) {
        error(`${file} backup integrity check failed, aborting.`);
        process.exit(1);
      }
      fileCount++;
    } else {
      if (copyFileIfExists(srcPath, dstPath)) fileCount++;
    }
  }

  // Copy session data directories (clean first to propagate deletions)
  for (const dir of SESSION_DATA_DIRS) {
    const src = path.join(dataRoot, dir);
    if (fs.existsSync(src)) {
      rmDirRecursive(path.join(repoData, dir));
      fileCount += copyDirRecursive(src, path.join(repoData, dir));
    }
  }

  // Export migration table for pull-side auto-repair
  const srcDbPath = path.join(dataRoot, "opencode.db");
  if (fs.existsSync(srcDbPath)) {
    writeMigrationMeta(repoData, srcDbPath);
    writeSessionOriginMeta(repoData, srcDbPath);
  }

  if (fileCount > 0) {
    // Silenced on success — summary will be shown by doPush
  }
}

function stageSessionDataOut(root) {
  const dataRoot = getDataRoot();
  const repoData = path.join(root, "_data");
  let fileCount = 0;

  ensureDir(dataRoot);

  // Snapshot local migrations BEFORE overwriting db (for auto-repair)
  const localDbPath = path.join(dataRoot, "opencode.db");
  const localMigrationsBefore = getMigrationRows(localDbPath);

  for (const file of SESSION_DATA_FILES) {
    const src = path.join(repoData, file);
    const dst = path.join(dataRoot, file);
    if (!fs.existsSync(src)) continue;

    // For .db files: remove stale WAL/SHM before overwriting
    if (file.endsWith(".db")) {
      rmFileIfExists(dst + "-wal");
      rmFileIfExists(dst + "-shm");
    }
    fs.copyFileSync(src, dst);
    fileCount++;
  }

  // Restore session data directories (clean first for local-consistent)
  for (const dir of SESSION_DATA_DIRS) {
    const src = path.join(repoData, dir);
    if (fs.existsSync(src)) {
      rmDirRecursive(path.join(dataRoot, dir));
      fileCount += copyDirRecursive(src, path.join(dataRoot, dir));
    }
  }

  if (fileCount > 0) {
    // Silenced on success — summary shown by doPull
  }

  // Post-restore integrity check
  for (const file of SESSION_DATA_FILES) {
    if (!file.endsWith(".db")) continue;
    verifySqlite(path.join(dataRoot, file));
  }

  // Auto-repair: re-insert local migration records missing in pulled db
  if (localMigrationsBefore.length) {
    repairMigrations(path.join(dataRoot, "opencode.db"), localMigrationsBefore);
  }
}

// ── Push / Pull ──────────────────────────────────────────────────────

function doPush(root, force, action, target) {
  ensureRepo(root);

  ensureFile(path.join(root, ".gitignore"), DEFAULT_GITIGNORE);
  ensureFile(path.join(root, ".gitattributes"), DEFAULT_GITATTRIBUTES);

  stageSessionDataIn(root);

  git("add -A", { cwd: root });

  const status = git("status --short", { cwd: root });
  if (!status.ok || !status.out) {
    success("Up to date");
    upToDateBanner(action, target);
    return;
  }

  const lines = status.out.split("\n");
  const added = lines.filter(
    (l) => l.startsWith("A ") || l.startsWith("?"),
  ).length;
  const modified = lines.filter((l) => l.startsWith("M ")).length;
  const deleted = lines.filter((l) => l.startsWith("D ")).length;
  const renamed = lines.filter((l) => l.startsWith("R ")).length;
  const totalChanges = added + modified + deleted + renamed;

  // Show staging result with stats
  success(`Staging files from data directory...  ${formatStats(added, modified, deleted, renamed)}`);
  // List changed files
  for (const line of lines) {
    const status_char = line.charAt(0);
    const filename = line.slice(3).trim();
    const prefix = status_char === "A" || status_char === "?" ? "+" : status_char === "D" ? "-" : "~";
    console.log(`${c.tn.gray}       ${prefix} ${filename}${c.reset}`);
  }

  const msg = commitMessage();
  const commitResult = git(`commit -m "${msg}"`, { cwd: root });
  if (!commitResult.ok) {
    error(`Commit failed: ${(commitResult.err || commitResult.out).split("\n")[0]}`);
    process.exit(1);
  }
  success(`Committed: ${totalChanges} file changes`);

  // Rebase + push
  const remoteRef = git(`rev-parse origin/${BRANCH}`, { cwd: root });
  if (!remoteRef.ok) {
    // First push
    const pushResult = git(`push -u origin ${BRANCH}`, { cwd: root });
    if (pushResult.ok) {
      success("Pushed to GitHub repo (first push)");
      completionBanner(action, target);
    } else {
      error(`Push failed: ${(pushResult.err || "").split("\n")[0]}`);
      process.exit(1);
    }
    return;
  }

  // Remote exists — rebase then push
  const pullResult = git(`pull --rebase origin ${BRANCH}`, { cwd: root });
  if (pullResult.ok) {
    const pushResult = git(`push origin ${BRANCH}`, { cwd: root });
    if (pushResult.ok) {
      success("Pushed to GitHub repo (rebase, no conflicts)");
      completionBanner(action, target);
    } else {
      error(`Push failed: ${(pushResult.err || "").split("\n")[0]}`);
      process.exit(1);
    }
    return;
  }

  // Rebase failed — likely conflict
  git("rebase --abort", { cwd: root });

  if (force) {
    const fpResult = git(`push --force-with-lease origin ${BRANCH}`, {
      cwd: root,
    });
    if (fpResult.ok) {
      success("Force-pushed to GitHub repo (force-with-lease)");
      completionBanner(action, target);
    } else {
      error(`Force push failed: ${(fpResult.err || "").split("\n")[0]}`);
      process.exit(1);
    }
  } else {
    error("Rebase conflict. Use --force to push anyway.");
    process.exit(1);
  }
}

function doPull(root, force, action, target) {
  if (!isGitRepo(root)) {
    const mode = initRepo(root);
    if (mode === "fresh") {
      console.log(`  ${c.tn.gray}📭 GitHub repo is empty. Push from another device first.${c.reset}`);
      return;
    }
    const files = git("ls-files", { cwd: root });
    const count = files.ok ? files.out.split("\n").filter(Boolean).length : 0;
    stageSessionDataOut(root);
    success(`First pull complete: 📄+${count}`);
    completionBanner(action, target);
    return;
  }

  ensureGitConfig(root);
  ensureLfs(root);

  // Check for local uncommitted changes → auto-stash
  git("add -A", { cwd: root });
  const status = git("diff --cached --stat", { cwd: root });
  const hasChanges = status.ok && status.out.length > 0;
  let didStash = false;

  if (hasChanges) {
    git("reset HEAD", { cwd: root });

    if (force) {
      git("checkout -- .", { cwd: root });
      git("clean -fd", { cwd: root });
    } else {
      git("add -A", { cwd: root });
      const stashResult = git('stash push -m "opencode-sync auto-stash"', {
        cwd: root,
      });
      if (stashResult.ok) {
        didStash = true;
      } else {
        error(`Stash failed: ${stashResult.err || stashResult.out}`);
        console.error("   Use --force to overwrite local changes");
        process.exit(1);
      }
    }
  }

  // Fetch
  success("Fetching from GitHub...");
  const fetchResult = git(`fetch origin ${BRANCH}`, { cwd: root });
  if (!fetchResult.ok) {
    error(`Fetch failed: ${fetchResult.err}`);
    process.exit(1);
  }

  const diff = git(`diff --stat HEAD origin/${BRANCH}`, { cwd: root });
  const hasIncoming = diff.ok && diff.out;

  if (hasIncoming) {
    const nameStatus = git(`diff --name-status HEAD origin/${BRANCH}`, {
      cwd: root,
    });
    const resetResult = git(`reset --hard origin/${BRANCH}`, { cwd: root });
    if (!resetResult.ok) {
      error(`Reset failed: ${resetResult.err}`);
      process.exit(1);
    }

    if (didStash) {
      const popResult = git("stash pop", { cwd: root });
      if (!popResult.ok) {
        error("Stash pop conflict");
        console.error("   Manual resolution:");
        console.error(`     cd "${root}"`);
        console.error("     git stash show          # review stashed changes");
        console.error("     git checkout -- .        # discard conflict markers");
        console.error("     git stash drop           # drop the stash");
        console.error("   Or use: opencode-pull-force");
        process.exit(1);
      }
    }

    stageSessionDataOut(root);
    const stats = formatDiffStats(nameStatus.ok ? nameStatus.out : "");
    success(`Pulled from GitHub: ${stats}`);
    completionBanner(action, target);
  } else {
    if (didStash) {
      const popResult = git("stash pop", { cwd: root });
      if (!popResult.ok) {
        error("Stash pop conflict");
        console.error("   Manual resolution:");
        console.error(`     cd "${root}"`);
        console.error("     git stash show          # review stashed changes");
        console.error("     git checkout -- .        # discard conflict markers");
        console.error("     git stash drop           # drop the stash");
        console.error("   Or use: opencode-pull-force");
        process.exit(1);
      }
    }

    success("Up to date");
    upToDateBanner(action, target);
  }
}

function cmdStatus(root) {
  if (!isGitRepo(root)) {
    console.log(`${c.gray}📭 Not initialized: ${root}${c.reset}`);
    console.log(`${c.gray}Run push or pull first${c.reset}`);
    return;
  }

  ensureGitConfig(root);
  const lastCommit = git("log --oneline -1", { cwd: root });
  const dbPath = path.join(getDataRoot(), "opencode.db");
  const dbSize = fs.existsSync(dbPath)
    ? `${(fs.statSync(dbPath).size / 1024 / 1024).toFixed(1)} MB`
    : "n/a";

  console.log(`${c.cyan}💾 Last sync${c.reset}  ${lastCommit.ok ? lastCommit.out : "No commits yet"}`);
  console.log(`${c.gray}Database size${c.reset}  ${dbSize}`);

  // Migration info
  const localRows = getMigrationRows(dbPath);
  const repoData = path.join(root, "_data");
  const metaPath = path.join(repoData, DB_META_FILE);
  const remoteMeta = fs.existsSync(metaPath)
    ? JSON.parse(fs.readFileSync(metaPath, "utf8"))
    : null;

  const localLabel = localRows.length > 0 ? `${localRows.length}` : "?";
  const remoteLabel =
    remoteMeta && remoteMeta.migrations
      ? `${remoteMeta.migrations.length} (${remoteMeta.hostname})`
      : "no records";
  console.log(`${c.gray}Migration records${c.reset}  local ${localLabel} / GitHub ${remoteLabel}`);
  if (
    remoteMeta &&
    remoteMeta.migrations &&
    localRows.length > remoteMeta.migrations.length
  ) {
    info("GitHub repo has fewer migration records; pull will restore missing local records");
  }

  // Show opencode processes
  const pids = findOpenCodePids();
  if (pids.length > 0) {
    warn(`Detected running OpenCode (PID: ${pids.join(", ")})`);
  } else {
    success("Clean: no running OpenCode processes");
  }
}

// ── Main ─────────────────────────────────────────────────────────────

async function main() {
  const args = process.argv.slice(2);
  const command = args[0];
  const force = args.includes("--force");
  const root = getConfigRoot();
  const target = (args.find(a => a.startsWith("--target=")) || "").replace("--target=", "") || "sessions";
  const action = (args.find(a => a.startsWith("--action=")) || "").replace("--action=", "") || command;

  if (!command || command === "help") {
    console.log("sync-sessions.js — Sync OpenCode session data to GitHub\n");
    console.log("⚠️ Must be run outside OpenCode (the script will terminate related processes).\n");
    console.log("Usage: node sync-sessions.js <push|pull|status> [--force]");
    return;
  }

  if (!fs.existsSync(root)) {
    if (command === "pull") {
      fs.mkdirSync(root, { recursive: true });
    } else {
      console.error(`Config directory not found: ${root}`);
      process.exit(1);
    }
  }

  switch (command) {
    case "push": {
      const ok = await ensureNoOpenCode();
      if (!ok) process.exit(1);
      doPush(root, force, action, target);
      break;
    }
    case "pull": {
      const ok = await ensureNoOpenCode();
      if (!ok) process.exit(1);
      doPull(root, force, action, target);
      break;
    }
    case "status":
      cmdStatus(root);
      break;
    default:
      console.error(`Unknown command: ${command}`);
      console.error("Available commands: push, pull, status");
      process.exit(1);
  }
}

main().catch((e) => {
  console.error("Unexpected error: ", e);
  process.exit(1);
});
